package com.meng.spring.pojo;

public class HdIngred {
    private Integer ingredId;

    private String ingredType;

    private String ingredName;

    private String ingredNum;

    private Integer cookId;

    public Integer getIngredId() {
        return ingredId;
    }

    public void setIngredId(Integer ingredId) {
        this.ingredId = ingredId;
    }

    public String getIngredType() {
        return ingredType;
    }

    public void setIngredType(String ingredType) {
        this.ingredType = ingredType;
    }

    public String getIngredName() {
        return ingredName;
    }

    public void setIngredName(String ingredName) {
        this.ingredName = ingredName;
    }

    public String getIngredNum() {
        return ingredNum;
    }

    public void setIngredNum(String ingredNum) {
        this.ingredNum = ingredNum;
    }

    public Integer getCookId() {
        return cookId;
    }

    public void setCookId(Integer cookId) {
        this.cookId = cookId;
    }
}